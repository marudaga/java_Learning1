package ch03.unit4;

public class OperatorEx01 {

	public static void main(String[] args) {
		int a, b;
		
		a = 10;b=5;
		a+=b;
		System.out.println(a);

		char c ='A';
		c+=10;
		System.out.println(c);
		
		float f=3.5f;
		c+=f;
		System.out.println(c);

		int n=4;
		n*=0.5;
		System.out.println(n);
		

	}

}
